// Get all departments
export const getDepartments = () => {
  return DEPARTMENTS_DATA.map((item) => item.department);
};

// Get sub-departments for selected departments
export const getSubDepartments = (selectedDepartments) => {
  if (!selectedDepartments || selectedDepartments.length === 0) {
    return [];
  }

  const subDepartments = [];
  selectedDepartments.forEach((department) => {
    const departmentData = DEPARTMENTS_DATA.find((item) => item.department === department);
    if (departmentData) {
      subDepartments.push(...departmentData.subDepartments);
    }
  });

  // Remove duplicates
  return [...new Set(subDepartments)];
};


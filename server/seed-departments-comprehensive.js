async function seedDepartments() {
    try {
        // Connect to MongoDB
        await mongoose.connect(process.env.MONGODB_URI || 'mongodb://localhost:27017/naukri-clone', {
            useNewUrlParser: true,
            useUnifiedTopology: true
        });
        console.log('Connected to MongoDB');

        // Clear existing departments
        await Department.deleteMany({});
        console.log('Cleared existing departments');

        // Insert new departments
        const departments = await Department.insertMany(departmentsData);
        console.log(`Successfully seeded ${departments.length} departments`);

        // Display summary
        console.log('\n=== DEPARTMENTS SEEDED ===');
        departments.forEach(dept => {
            console.log(`${dept.name}: ${dept.subcategories.length} sub-departments`);
        });

        console.log('\n=== SUMMARY ===');
        console.log(`Total Departments: ${departments.length}`);
        console.log(`Total Sub-departments: ${departments.reduce((sum, dept) => sum + dept.subcategories.length, 0)}`);

    } catch (error) {
        console.error('Error seeding departments:', error);
    } finally {
        await mongoose.disconnect();
        console.log('Disconnected from MongoDB');
    }
}

// Run the seeding function
if (require.main === module) {
    seedDepartments();
}

module.exports = { seedDepartments, departmentsData };
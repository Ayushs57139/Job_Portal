async function seedJobTitles() {
  try {
    console.log('Starting to seed job titles...');
    
    // Clear existing job titles
    await JobTitle.deleteMany({});
    console.log('Cleared existing job titles');
    
    // Create a dummy user ID for createdBy field
    const dummyUserId = new mongoose.Types.ObjectId();
    
    // Prepare data with required fields
    const jobTitlesToInsert = jobTitlesData.map(jobTitle => ({
      name: jobTitle.name,
      category: jobTitle.category,
      isActive: true,
      isVerified: true,
      addedBy: dummyUserId,
      usageCount: 1,
      lastUsed: new Date()
    }));
    
    // Insert all job titles
    const result = await JobTitle.insertMany(jobTitlesToInsert, { ordered: false });
    console.log(`Successfully inserted ${result.length} job titles`);
    
    // Display summary by category
    const categorySummary = {};
    result.forEach(jobTitle => {
      categorySummary[jobTitle.category] = (categorySummary[jobTitle.category] || 0) + 1;
    });
    
    console.log('\nJob titles by category:');
    Object.entries(categorySummary).forEach(([category, count]) => {
      console.log(`${category}: ${count} titles`);
    });
    
    console.log('\nSeeding completed successfully!');
    
  } catch (error) {
    console.error('Error seeding job titles:', error);
  } finally {
    mongoose.connection.close();
  }
}

// Run the seeding function
seedJobTitles();
